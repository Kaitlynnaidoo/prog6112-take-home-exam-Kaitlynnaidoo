    private void ProcessReportMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProcessReportMenuItemActionPerformed

        String location = (String) AgentLocationComboBox.getSelectedItem();
        String name = EstateAgentNameTxtField.getText();
        String Price = PropertyPriceTxtField.getText();
        String CommissionPercent = CommissionPercentageTxtField.getText();

        Data data = new Data(location, name, Price, CommissionPercent);

        EstateAgent agent = new EstateAgent();

        if (agent.ValidateData(data)) {
            String message = "";
            message += "ESTATE AGENT REPORT\n";
            message += "*********************************************\n";
            message += "AGENT LOCATION: " + location + "\n";
            message += "AGENT NAME: " + name + "\n";
            message += "PROPERTY PRICE: R " + Price + "\n";
            message += "COMMISSION PERCENTAGE: " + CommissionPercent + "%\n";
            message += "CALCULATED COMMISSION: R " + agent.CalculateCommission(Price, CommissionPercent) + "\n";
            message += "*********************************************";

            EstateAgentReportTextArea.setText(message);
        }

    }//GEN-LAST:event_ProcessReportMenuItemActionPerformed

