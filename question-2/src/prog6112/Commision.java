    private void SaveReportMenuItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveReportMenuItemActionPerformed
        try {
            FileWriter fw = new FileWriter("report.txt");
            var pw = new PrintWriter(fw);

            String location = (String) AgentLocationComboBox.getSelectedItem();
            String name = EstateAgentNameTxtField.getText();
            String Price = PropertyPriceTxtField.getText();
            String CommissionPercent = CommissionPercentageTxtField.getText();

            Data data = new Data(location, name, Price, CommissionPercent);

            EstateAgent agent = new EstateAgent();

            if (agent.ValidateData(data)) {
                String message = "";
                pw.append("ESTATE AGENT REPORT\n");
                pw.append("*********************************************\n");
                pw.append("AGENT LOCATION: " + location + "\n");
                pw.append("AGENT NAME: " + name + "\n");
                pw.append("PROPERTY PRICE: R " + Price + "\n");
                pw.append("COMMISSION PERCENTAGE: " + CommissionPercent + "%\n");
                pw.append("CALCULATED COMMISSION: R " + agent.CalculateCommission(Price, CommissionPercent) + "\n");
                pw.append("*********************************************");

            }

            pw.close();
            fw.close();
        } catch (IOException ex) {
        }

    }//GEN-LAST:event_SaveReportMenuItemActionPerformed

